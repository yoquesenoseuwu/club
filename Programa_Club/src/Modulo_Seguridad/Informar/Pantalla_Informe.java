    public void Mostrar_Informe_Seleccionado (int id){ //Muestra el informa seleccionado
        modelo.removeAllElements();
        ArrayList array=conexion.Select_unique_informe(id);
        
        String Id=(String) array.get(0);
        id_informe=id;
        String Fecha=(String) array.get(1);
        String Autor=(String) array.get(2);
        String Titulo=(String) array.get(3);
        String Texto=(String) array.get(4);
        
        jTextArea1.append(Id+"\n"+Fecha+"\n"+Autor+"\n"+Titulo+"\n"+Texto+"\n");
    }

